let currentLang = 'pt-BR';

function updateContent() {
    const isPrivacy = window.location.pathname.includes('privacy.html');
    const langData = legalTranslations[currentLang];
    const pageData = isPrivacy ? langData.privacy : langData.terms;

    // Update common UI elements
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (langData[key]) el.innerHTML = langData[key];
    });

    // Update Title and Last Update
    document.querySelector('h1').innerHTML = pageData.title;
    document.querySelector('p strong').parentElement.innerHTML = `<strong>${pageData.lastUpdate.split(':')[0]}:</strong>${pageData.lastUpdate.split(':')[1]}`;

    // Render legal content
    const container = document.querySelector('.legal-content');
    if (isPrivacy) {
        renderPrivacy(container, pageData);
    } else {
        renderTerms(container, pageData);
    }

    // Update back button
    const backBtn = document.querySelector('.back-button');
    if (backBtn) {
        const svg = backBtn.querySelector('svg').outerHTML;
        backBtn.innerHTML = svg + ' ' + pageData.backBtn;
    }
}

