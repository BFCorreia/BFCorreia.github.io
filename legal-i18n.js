// Language Switch Event Listeners (Simplified for legal pages)
document.addEventListener('DOMContentLoaded', () => {
    // Add language switcher to the top right
    const nav = document.createElement('nav');
    nav.className = 'fixed top-4 right-4 z-50 glass-effect p-1 rounded-lg flex gap-1';
    nav.innerHTML = `
        <button id="lang-pt" class="px-2 py-1 text-xs rounded transition bg-indigo-600 text-white">PT</button>
        <button id="lang-en" class="px-2 py-1 text-xs rounded transition text-gray-400 hover:text-white">EN</button>
    `;
    document.body.appendChild(nav);

    const btnPt = document.getElementById('lang-pt');
    const btnEn = document.getElementById('lang-en');

    btnPt.addEventListener('click', () => {
        currentLang = 'pt-BR';
        btnPt.classList.add('bg-indigo-600', 'text-white');
        btnPt.classList.remove('text-gray-400', 'hover:text-white');
        btnEn.classList.remove('bg-indigo-600', 'text-white');
        btnEn.classList.add('text-gray-400', 'hover:text-white');
        updateContent();
    });

    btnEn.addEventListener('click', () => {
        currentLang = 'en';
        btnEn.classList.add('bg-indigo-600', 'text-white');
        btnEn.classList.remove('text-gray-400', 'hover:text-white');
        btnPt.classList.remove('bg-indigo-600', 'text-white');
        btnPt.classList.add('text-gray-400', 'hover:text-white');
        updateContent();
    });

    updateContent();
});
